const LanguageContext = createContext<LanguageContextType>({
  language: 'bn',
  setLanguage: () => {},
  t: (key: string) => key,
});

export const useLanguage = () => useContext(LanguageContext);

export const LanguageProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const { currentUser } = useAuth();
  
  // Try to get from local storage first for instant load
  const [language, setLanguageState] = useState<Language>(() => {
    const saved = localStorage.getItem('appLanguage');
    return (saved === 'en' || saved === 'bn') ? saved : 'bn';
  });

  // Then check Firestore user preferences
  useEffect(() => {
    const fetchUserLang = async () => {
      if (!currentUser) return;
      try {
        const docRef = doc(db, 'users', currentUser.uid);
        const snap = await getDoc(docRef);
        if (snap.exists() && snap.data().language) {
          const userLang = snap.data().language;
          if (userLang === 'bn' || userLang === 'en') {
            setLanguageState(userLang as Language);
            localStorage.setItem('appLanguage', userLang);
          }
        }
      } catch (err) {
        console.error(err);
      }
    };
    fetchUserLang();
  }, [currentUser]);

  const setLanguage = (lang: Language) => {
    setLanguageState(lang);
    localStorage.setItem('appLanguage', lang);
  };

  const t = (key: string): string => {
    return translations[language][key] || key;
  };

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t }}>
      {children}
    </LanguageContext.Provider>
  );
};
